document.addEventListener('DOMContentLoaded', function () {
  const searchBox = document.getElementById('search-box');
  const bookContainer = document.getElementById('book-container');
  const books = Array.from(document.querySelectorAll('.swiper-slide'));

  function searchBooks() {
      const query = searchBox.value.toLowerCase().trim();

      const matchingBooks = books.filter(book => {
          const title = book.getAttribute('data-title');
          return title && title.toLowerCase().includes(query);
      });

      bookContainer.innerHTML = '';

      if (matchingBooks.length > 0) {
          matchingBooks.forEach(book => bookContainer.appendChild(book));
      } else {
          bookContainer.innerHTML = '<p>No books found</p>';
      }

      books.forEach(book => {
          if (!matchingBooks.includes(book)) {
              bookContainer.appendChild(book);
          }
      });
  }

  searchBox.addEventListener('keyup', searchBooks);
});
